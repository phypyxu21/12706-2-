#内衬层与填充
#内衬层可以挤包或绕包,挤包内衬层前允许用合适的带子扎紧。
#圆形绝缘线芯电缆只有在绝缘线芯间的间隙被密实填充时,才应允许采用绕包内衬层。
#挤包内衬层厚度
"""
缆芯假设直径/mm	挤包内衬层厚度近似值/mm  
—	    ≤25	1.0  
＞25	≤35	1.2  
＞35	≤45	1.4  
＞45	≤60	1.6  
＞60	≤80	1.8  
＞80	—	2.0  """

def get_coefficient(value):
    """
    根据输入的数值返回对应的系数
    :param value: 缆芯假设直径/mm
    :return: 对应挤包内衬层厚度近似值/mm 
    """
    # 如果数值小于等于25，返回对应挤包内衬层厚度近似值1.0
    if value <= 25:
        return 1.0
    # 如果数值在25到35之间，返回对应挤包内衬层厚度近似值1.2
    elif 25 < value <= 35:
        return 1.2
    # 如果数值在35到45之间，返回对应挤包内衬层厚度近似值1.4
    elif 35 < value <= 45:
        return 1.4
    # 如果数值在45到60之间，返回对应挤包内衬层厚度近似值1.6
    elif 45 < value <= 60:
        return 1.6
    # 如果数值在60到80之间，返回对应挤包内衬层厚度近似值1.8
    elif 60 < value <= 80:
        return 1.8
    else:  # 大于80的情况，返回对应挤包内衬层厚度近似值2.0
        return 2.0

# 绕包内衬层厚度
#缆芯假设直径为 40 mm 及以下时,绕包内衬层的近似厚度取 0.4 mm ; 若缆芯假设直径大于 40 mm 绕包内衬层的近似厚度取0.6 mm 。
def get_liner_thickness(core_diameter):
    """
    根据缆芯直径计算绕包内衬层的近似厚度
    
    参数:
        core_diameter (float): 缆芯的直径，单位为毫米(mm)
        
    返回:
        float: 绕包内衬层的近似厚度，单位为毫米(mm)
    """
    if core_diameter <= 40:
        return 0.4
    else:
        return 0.6



